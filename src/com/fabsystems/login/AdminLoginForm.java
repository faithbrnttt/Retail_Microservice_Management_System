    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        if(validateAdminLoginInputs()== false){
            return;
        }
        AdminLoginIO alio = new AdminLoginIO();
        String username = txtAdminUsername.getText();
        String password = pwfAdminPassword.getText();
        try {
            alio.adminAuth(username, password);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Driver Error.", JOptionPane.ERROR_MESSAGE);
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error.", JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_btnLoginActionPerformed

