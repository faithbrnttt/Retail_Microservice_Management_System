    //validate username and password for general access
    public boolean validateAdminLoginInputs(){
        String username = txtAdminUsername.getText();
        String password = pwfAdminPassword.getText();
        
        if (username.isBlank()) {
            JOptionPane.showMessageDialog(this, "Username cannot be blank.",
                    "Name Error", JOptionPane.ERROR_MESSAGE);
            txtAdminUsername.requestFocusInWindow();
            return false;
        }
        if (password.isBlank()) {
            JOptionPane.showMessageDialog(this, "Password cannot be blank.",
                    "Name Error", JOptionPane.ERROR_MESSAGE);
            pwfAdminPassword.requestFocusInWindow();
            return false;
        }
       return true;
    }
